package Objects;

public class Goat implements Animal {

    public Goat() {
    }

    public void sound() {
        System.out.println("Goat says meee.");
    }

    public void giveMilk() {
        System.out.println("Goat gives milk.");
    }

    public void birth() {
        System.out.println("Goat gives birth to a kid.");
    }
}
